package com.rojaware.query.model;

public class EnvBean {

    private String env;

    public EnvBean() {
		super();
		
	}

	public EnvBean(String env) {
		super();
		this.env = env;
	}

	public String getEnv() {
        return env;
    }

    public void setEnv(String env) {
        this.env = env;
    }
}